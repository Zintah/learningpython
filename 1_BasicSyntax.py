# Basic Types

# Integer variable
age = 25
print(age) 

# String variable
name = "Alice"
print(name) 

# Float variable
height = 5.8
print(height) 

# Boolean variable
is_student = True
print(is_student)  

# Examples of basic data types
num1 = 42  # Integer
num2 = 3.14159  # Float
text = "Python is fun!"  # String
is_active = False  # Boolean
print(num1, num2, text, is_active)

